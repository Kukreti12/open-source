CREATE TABLE IF NOT EXISTS bitcoin (
    symbol VARCHAR(250),
    baseAsset VARCHAR(250),
    quoteAsset VARCHAR(250),
    openPrice VARCHAR(250),
    lowPrice VARCHAR(250),
    highPrice  VARCHAR(250),
    lastPrice VARCHAR(250),
    volume VARCHAR(250),
    bidPrice  VARCHAR(250),
    askPrice  VARCHAR(250),
    at VARCHAR(250)
    );